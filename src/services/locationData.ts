export const normalizeLocationLookup = (value?: string | null): string =>
  String(value || '').trim().toLocaleLowerCase('tr');

export const resolveCanonicalCity = (value?: string | null): string | null => {
  const normalized = normalizeLocationLookup(value);

  if (!normalized) {
    return null;
  }

  return (
    TURKEY_CITIES.find(
      (city) => city.toLocaleLowerCase('tr') === normalized
    ) ?? null
  );
};

export const resolveTurkeyCityCode = (city?: string | null): string | null => {
  const canonicalCity = resolveCanonicalCity(city);

  if (!canonicalCity) {
    return null;
  }

  return TURKEY_CITY_CODES[canonicalCity] ?? null;
};

export const getDistrictsByCity = (city?: string): string[] => {
  if (!city) return [];
  return [...(TURKEY_DATA[city] || [])].sort((a, b) => a.localeCompare(b, 'tr'));
};

export const resolveCanonicalDistrict = (
  city?: string | null,
  district?: string | null
): string | null => {
  const normalizedCity = resolveCanonicalCity(city);
  const normalizedDistrict = normalizeLocationLookup(district);

  if (!normalizedCity || !normalizedDistrict) {
    return null;
  }

  return (
    getDistrictsByCity(normalizedCity).find(
      (item) => item.toLocaleLowerCase('tr') === normalizedDistrict
    ) ?? null
  );
};

export const isValidCity = (city?: string): boolean => {
  if (!city) return false;
  return Object.prototype.hasOwnProperty.call(TURKEY_DATA, city);
};

export const isValidDistrict = (city?: string, district?: string): boolean => {
  if (!city || !district) return false;
  return (TURKEY_DATA[city] || []).includes(district);
};

export const searchCities = (query?: string): string[] => {
  const q = String(query || '').trim().toLocaleLowerCase('tr');
  if (!q) return [...TURKEY_CITIES];

  return TURKEY_CITIES.filter((city) =>
    city.toLocaleLowerCase('tr').includes(q)
  );
};

export const searchDistricts = (city?: string, query?: string): string[] => {
  const districts = getDistrictsByCity(city);
  const q = String(query || '').trim().toLocaleLowerCase('tr');

  if (!q) return districts;

  return districts.filter((district) =>
    district.toLocaleLowerCase('tr').includes(q)
  );
};
